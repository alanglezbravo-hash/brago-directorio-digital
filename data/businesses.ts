export function getBusinessBySlug(slug: string): Business | undefined {
  return businesses.find((b) => b.slug === slug)
}

export function getFeaturedBusinesses(): Business[] {
  return businesses.filter((b) => b.featured)
}

export function getRelatedBusinesses(current: Business, limit = 3): Business[] {
  return businesses
    .filter(
      (b) =>
        b.id !== current.id &&
        (b.category === current.category || b.city === current.city)
    )
    .slice(0, limit)
}

